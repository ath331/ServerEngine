///////////////////////////////////////////////////////////////////////////
// @brief     PktLogin
///////////////////////////////////////////////////////////////////////////


#include "pch.h"
#include "PktLogin.h"


///////////////////////////////////////////////////////////////////////////
// @brief     패킷 사이즈를 세팅한다.
///////////////////////////////////////////////////////////////////////////
void PktLogin::SetSize()
{
	WriteStream tempWriteStream;

	super::Serialize( tempWriteStream );
	tempWriteStream << m_id;
	tempWriteStream << m_pw;

	m_size = tempWriteStream.GetSize();
}

///////////////////////////////////////////////////////////////////////////
// @brief     직렬화
///////////////////////////////////////////////////////////////////////////
void PktLogin::Serialize( WriteStream& writeStream )
{
	SetSize();

	super::Serialize( writeStream );
	writeStream << m_id;
	writeStream << m_pw;
}

///////////////////////////////////////////////////////////////////////////
// @brief     역직렬화
///////////////////////////////////////////////////////////////////////////
void PktLogin::Deserialize( ReaderStream& readerStream )
{
	super::Deserialize( readerStream );

	readerStream >> m_id;
	readerStream >> m_pw;
}
