///////////////////////////////////////////////////////////////////////////
// @brief     PktLogin
///////////////////////////////////////////////////////////////////////////


#include "pch.h"
#include "PktLogin.h"


///////////////////////////////////////////////////////////////////////////
// @brief     직렬화
///////////////////////////////////////////////////////////////////////////
void PktLogin::Serialize( WriteStream& writeStream )
{
	super::Serialize( writeStream );

	writeStream << m_id;
	writeStream << m_pw;

	super::SetSize( writeStream.GetSize() );
}

///////////////////////////////////////////////////////////////////////////
// @brief     역직렬화
///////////////////////////////////////////////////////////////////////////
void PktLogin::Deserialize( ReaderStream& readerStream )
{
	super::Deserialize( readerStream );

	readerStream >> m_id;
	readerStream >> m_pw;
}
