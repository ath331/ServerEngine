#include "pch.h"
#include "PacketBase.h"


///////////////////////////////////////////////////////////////////////////
// @brief     int 타입 직렬화 오버로딩
///////////////////////////////////////////////////////////////////////////
ostream& PacketBase::operator<<( int data )
{
	_Write( &data, sizeof( data ) );
}

///////////////////////////////////////////////////////////////////////////
// @brief     string 타입 직렬화 오버로딩
///////////////////////////////////////////////////////////////////////////
ostream& PacketBase::operator<<( const string& data )
{
	int strSize = data.length();
	_Write( &strSize, sizeof( strSize ) );

	_Write( data.c_str(), sizeof( data ) );
}