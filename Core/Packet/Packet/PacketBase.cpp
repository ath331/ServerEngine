#include "pch.h"
#include "PacketBase.h"



///////////////////////////////////////////////////////////////////////////
// @brief     직렬화
///////////////////////////////////////////////////////////////////////////
void PacketBase::Serialize( WriteStream& writeStream )
{
	writeStream << m_pktId;
}

///////////////////////////////////////////////////////////////////////////
// @brief     역직렬화
///////////////////////////////////////////////////////////////////////////
void PacketBase::Deserialize( ReaderStream& readerStream )
{
	readerStream >> m_pktId;
}
