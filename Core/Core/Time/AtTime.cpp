#include "pch.h"
#include "AtTime.h"


///////////////////////////////////////////////////////////////////////////
// @brief     1970년 1월 1일 자정 이후부터 현재까지 흐른 Second를 반환한다.
///////////////////////////////////////////////////////////////////////////
Second AtTime::GetCurTime()
{
	auto timepoint = std::chrono::system_clock::now();

	return std::chrono::duration_cast< Second >( timepoint.time_since_epoch() );
}
