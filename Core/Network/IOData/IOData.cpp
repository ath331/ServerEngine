#include "pch.h"
#include "IOData.h"


///////////////////////////////////////////////////////////////////////////
// @brief     생성자
///////////////////////////////////////////////////////////////////////////
AnT::IOData::IOData( EIOMode ioMode )
	: m_ioMode( ioMode )
{
	memset( this, 0, sizeof( IOData* ) );

	InitializeOverlapped();

	m_wsaBuf.len = BUF_SIZE;
	m_wsaBuf.buf = m_buffer;

	// cout << "@@@@@@@ IOData Make @@@@@@" << endl;
}

///////////////////////////////////////////////////////////////////////////
// @brief     OVERLAPPED 초기화
///////////////////////////////////////////////////////////////////////////
void AnT::IOData::InitializeOverlapped()
{
	memset( &( m_overlapped ), 0, sizeof( OVERLAPPED ) );
}
