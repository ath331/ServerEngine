///////////////////////////////////////////////////////////////////////////
// @brief     SocketDataManager Class
///////////////////////////////////////////////////////////////////////////


#include "pch.h"
#include "SocketDataManager.h"


///////////////////////////////////////////////////////////////////////////
// @brief     소켓 데이터를 목록에 추가
///////////////////////////////////////////////////////////////////////////
void AnT::SocketDataManager::AddSocketData( SocketData* socketData )
{
	if ( !socketData )
		return;

	m_socketDataMap[ socketData->sock ] = socketData;
}

///////////////////////////////////////////////////////////////////////////
// @brief     소켓 데이터를 목록에서 삭제
///////////////////////////////////////////////////////////////////////////
void AnT::SocketDataManager::DeleteSocket( SocketData* socketData )
{
	if ( !socketData )
		return;

	m_socketDataMap.erase( socketData->sock );

	delete( socketData );
	socketData = nullptr;
}
